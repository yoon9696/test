package test05;

public class DateType2 {

	public static void main(String[] args) {
		//Math : 수학
		//올림,  반올림 ,  내림 
		//ceil, round,  floor
		double d1 = 1.23456789;
		System.out.println(Math.ceil(d1)); // 2.0 올림
		System.out.println(Math.round(d1));// 1 반올림
		System.out.println(Math.floor(d1));// 1.0 내림
	}
}
